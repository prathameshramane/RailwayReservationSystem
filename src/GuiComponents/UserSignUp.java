    private boolean verifyData() {
        UserQuery uq = new UserQuery();
        if (jTextFieldFirstName.getText().equals("")
                || jTextFieldLastName.getText().equals("")
                || jTextFieldAdharNo.getText().equals("")
                || jComboBoxGender.getSelectedItem().equals("")
                || jTextFieldAge.getText().equals("")
                || jTextFieldMobile.getText().equals("")
                || jTextFieldEmail.getText().equals("")
                || jTextFieldCity.getText().equals("")
                || jTextFieldState.getText().equals("")
                || jTextFieldPincode.getText().equals("")
                || jTextFieldUsername.getText().equals("")
                || String.valueOf(jPasswordFieldPassword.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Please fill in all the fields");
            return false;
        } else if (uq.isUserExists(jTextFieldUsername.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Username " + jTextFieldUsername.getText() + " already exists.");
            return false;
        } else {
            return true;
        }
    }
    
