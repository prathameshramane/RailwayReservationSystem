    private void jButtonSignupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSignupBtnActionPerformed
        // TODO add your handling code here:
        if (verifyData()) {
            UserQuery uq = new UserQuery();
            uq.createNewUser(jTextFieldFirstName.getText(),
                    jTextFieldLastName.getText(),
                    jTextFieldAdharNo.getText(),
                    String.valueOf(jComboBoxGender.getSelectedItem()),
                    Integer.parseInt(jTextFieldAge.getText()),
                    jTextFieldMobile.getText(),
                    jTextFieldEmail.getText(),
                    jTextFieldCity.getText(),
                    jTextFieldState.getText(),
                    jTextFieldPincode.getText(),
                    jTextFieldUsername.getText(),
                    String.valueOf(jPasswordFieldPassword.getPassword()),
                    "",
                    "");
            JOptionPane.showMessageDialog(this, "User account created. Please login now");
            clearFields();
        }
    }//GEN-LAST:event_jButtonSignupBtnActionPerformed

